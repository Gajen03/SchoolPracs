public class MainLoop{
     public static void main(String[] args){
     
     int i = 10;
     for(i=10;i>0;i--){
     System.out.println(i);
     System.out.println();
     }
      int j = 25;
     for(j=25;j<=30;j++){
     System.out.println(j);
     System.out.println();
     }

         int n = 10;
     for(n=0;n>=-20;n--){
     System.out.println(n);
     System.out.println();
     }
    
   }
 }